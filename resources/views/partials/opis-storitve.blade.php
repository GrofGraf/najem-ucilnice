<h1>Najem učilnice</h1>
<h2>Ponujamo možnost najema učilnice.</h2>
<h3>Učilnica je velika 85 m2, sodobno opremljena in uporabnikom prijazna. Med odmori imajo udeleženci na voljo tudi napitke iz kavomata.</h3>
<p>
  Ob dolgoročnem najemu se cena določi posebej. Glede na predvideno število ur najema, se priznavajo določeni popusti. Pošljite povpraševanje.
</p>
